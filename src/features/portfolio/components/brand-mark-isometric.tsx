const PRESS_DEPTH = 12.0

export function BrandMarkIsometric() {
 const id = useId()
 const ids = {
 facePattern: `pkki-face-pattern-${id}`,
 radialGradient: `pkki-radial-gradient-${id}`,
 }

 const ref = useRef<SVGSVGElement>(null)

 const [play] = useSound(metalClickSound)

 const shouldReduceMotion = useReducedMotion()
 const isInView = useInView(ref, { margin: "80px" })

 const mouseX = useMotionValue(0.5)
 const mouseY = useMotionValue(0.5)

 const cx = useSpring(useTransform(mouseX, [0, 1], [0, 553.0]), {
 stiffness: 150,
 damping: 25,
 })

 const cy = useSpring(useTransform(mouseY, [0, 1], [0, 363.0]), {
 stiffness: 150,
 damping: 25,
 })

 useEffect(() => {
 if (shouldReduceMotion || !isInView) {
 return
 }

 if (window.matchMedia("(hover: none)").matches) {
 return
 }

 const handleMouseMove = (e: MouseEvent) => {
 mouseX.set(e.clientX / window.innerWidth)
 mouseY.set(e.clientY / window.innerHeight)
 }

 window.addEventListener("mousemove", handleMouseMove)

 return () => {
 window.removeEventListener("mousemove", handleMouseMove)
 }
 }, [shouldReduceMotion, isInView, mouseX, mouseY])

 return (
 <motion.svg
 ref={ref}
 className="h-auto w-full touch-manipulation overflow-visible [--pattern:color-mix(in_oklab,var(--foreground)_12%,var(--background))] [--stroke:color-mix(in_oklab,var(--foreground)_16%,var(--background))]"
 viewBox="0 0 553.0 363.0"
 fill="none"
 xmlns="http://www.w3.org/2000/svg"
 aria-hidden
 initial="normal"
 whileTap="pressed"
 onTap={() => play()}
 >
 <defs>
 <pattern
 id={ids.facePattern}
 x="0"
 y="0"
 width="10"
 height="10"
 patternUnits="userSpaceOnUse"
 >
 <path
 d="M-1 1l2 -2M0 10l10 -10M9 11l2 -2"
 stroke="var(--pattern)"
 strokeWidth="1"
 />
 </pattern>

 <motion.radialGradient
 id={ids.radialGradient}
 cx={cx}
 cy={cy}
 r="200"
 gradientUnits="userSpaceOnUse"
 >
 <stop
 className="dark:[stop-color:#fff]"
 stopColor="var(--color-zinc-700)"
 />
 <stop
 className="dark:[stop-color:var(--color-zinc-600)]"
 offset="1"
 stopColor="var(--color-zinc-400)"
 stopOpacity="0"
 />
 </motion.radialGradient>
 </defs>

 <g className="stroke-line" strokeWidth="1" strokeDasharray="4 2">
 <path d="M-477.55 756.57L1254.51 -243.41" />
 <path d="M977.37 788.58L-754.67 -211.42" />
 <path d="M1143.65 692.58L-588.39 -307.42" />
 </g>

 {/* Extruded sides */}
 <motion.path
 className="fill-background"
 variants={{
 normal: { d: SIDES_NORMAL },
 pressed: { d: SIDES_PRESSED },
 }}
 transition={transition}
 />
 <motion.path
 variants={{
 normal: { d: SIDES_NORMAL },
 pressed: { d: SIDES_PRESSED },
 }}
 transition={transition}
 stroke="var(--stroke)"
 />
 <motion.path
 variants={{
 normal: { d: SIDES_NORMAL },
 pressed: { d: SIDES_PRESSED },
 }}
 transition={transition}
 stroke={`url(#${ids.radialGradient})`}
 />

 {/* Letter top faces */}
 <motion.g
 variants={{
 normal: { transform: "translate(0px, 0px)" },
 pressed: { transform: `translate(0px, ${PRESS_DEPTH}px)` },
 }}
 transition={transition}
 >
 <path className="fill-background" d={TOPS_PATH} />
 <path fill={`url(#${ids.facePattern})`} d={TOPS_PATH} />
 <path stroke="var(--stroke)" d={TOPS_PATH} />
 <path stroke={`url(#${ids.radialGradient})`} d={TOPS_PATH} />
 </motion.g>
 </motion.svg>
 )
}
